 แบบเก่า: การใช้คำนำหน้า (Prefix)
 ก่อนที่ PHP 5.3 จะมีการใช้งาน namespace, นักพัฒนามักใช้คำนำหน้ากับชื่อฟังก์ชันและคลาสเพื่อหลีกเลี่ยงการชนกันของชื่อใน global namespace.
 
    <?php
    // การใช้คำนำหน้ากับฟังก์ชัน
    function myplugin_get_post() {
        // โค้ดที่นี่
    }

    // การใช้คำนำหน้ากับคลาส
    class MyPlugin_Setup {
        // โค้ดของคลาสที่นี่
    }

    แบบใหม่: การใช้งาน Namespace
หลังจาก PHP 5.3, namespace ถูกนำมาใช้เพื่อจัดกลุ่มโค้ดและหลีกเลี่ยงการชนกันของชื่อฟังก์ชันหรือคลาสในโปรเจกต์ที่ใหญ่ขึ้น

<?php
namespace MyPlugin;

class Setup {
    // โค้ดของคลาสที่นี่
}

function get_post() {
    // โค้ดที่นี่
}




